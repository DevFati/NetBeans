     public void mostrarNota(Double nota){
      
        if(nota<5){
           jLabel1.setText("Su nota es de un "+nota+". \n"
                   + "¡Felicidades! Ahora tienes una excusa \n"
                   + "perfecta para volver a intentarlo... \n"
                   + " porque la primera vez era solo\n "
                   + "un ensayo, ¿verdad?\n"); 
        }else if(nota>=5 || nota<=6){
            jLabel1.setText("¡Un "+nota+"! \n Eso es lo que yo llamo equilibrar \n"
                    +       "perfectamente entre hacer algo y no hacer nada.");
        }else if(nota>6 ||nota<=8){
            jLabel1.setText("¡Un "+nota+"! \nLo justo para demostrar que te importa... "
                    + "\npero no tanto como para dejar de ver series.");
        }else{
            jLabel1.setText("¡Un "+nota+"! \n No sé si felicitarte o pedirte que "
                    +       "\nme enseñes tus trucos de magia.");
        }
        
    }
   

